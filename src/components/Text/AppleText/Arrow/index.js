/**
 * Получает компонент Arrow на основе direction, variant и weight
 * @param {string} direction - направление стрелки ("up" или "down")
 * @param {string} variant - вариант текста (например, "body", "title1")
 * @param {string} weight - вес текста (например, "regular", "medium", "semibold")
 * @returns {React.Component|null} - компонент Arrow или null, если не найден
 */
export const getArrow = (direction, variant, weight = "regular") => {
    if (!direction || !variant) return null

    const directionKey = direction.toLowerCase()
    const variantKey = variant.toLowerCase()
    const weightKey = weight.toLowerCase()

    return arrowMap[directionKey]?.[variantKey]?.[weightKey] || null
}

export default arrowMap
